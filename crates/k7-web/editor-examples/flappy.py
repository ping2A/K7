def gfx_to_sheet(hex_str):
    lines = [L for L in hex_str.strip().split("\
") if len(L) >= 128]
    sheet = bytearray(256 * 64)
    for y in range(min(64, len(lines))):
        for x in range(128):
            sheet[y * 256 + x] = int(lines[y][x], 16)
    return bytes(sheet)


CANVAS_ID = "k7canvas"
W, H = 256, 256
SCALE = 2
pw, ph = 128, 128

state = 0
px, py = 30, 60
pv, pa = 0.0, 0.0
pr, g = 0.001, 0.5
gap = 6
pipex = 128
pipey = 0
score = 0
f = 0
bkgx = 0.0

def init():
    global state, px, py, pv, pa, pipex, pipey, score, f, bkgx
    state = 0
    px, py = 30, 60
    pv, pa = 0.0, 0.0
    pipex, pipey = 128, random.randint(0, 15 - gap)
    score = 0
    f = 0
    bkgx = 0.0

    sheet = gfx_to_sheet(GFX_HEX)
    k7.set_sprite_sheet(sheet)

def update():
    global state, px, py, pv, pa, pipex, pipey, score, f, bkgx
    f = (f + 1) & 255
    bkgx += 1
    if state == 0:
        if k7.btnp(4) or k7.btnp(5):
            pv, pa = 0.0, -5.0
            state = 1
    elif state == 1:
        pipex -= 1
        if pipex <= -24:
            pipey = random.randint(0, 15 - gap)
            pipex = 128
        pa *= pr
        pv += g + pa
        py += pv
        if py > 120 or py < -8:
            state = 2
        if (px + 6 > pipex and px + 2 < pipex + 24) and (py + 2 < (pipey + 1) * 8 or py + 6 > (pipey + 1 + gap) * 8):
            state = 2
        if pipex == 24:
            score += 1
        if k7.btnp(4) or k7.btnp(5):
            pv, pa = 0.0, -5.0
    elif state == 2:
        if f < 10:
            k7.camera(random.randint(-2, 2), random.randint(-2, 2))
        else:
            pa *= pr
            pv += g + pa
            py += pv
        if f >= 10 and py > 128:
            state = 3
    elif state == 3:
        k7.camera(0, 0)
        if k7.btnp(4) or k7.btnp(5):
            py, pv = 60, 0.0
            pipex = -24
            score = 0
            state = 1

def draw():
    k7.cls(12)
    for i in range(-2, 4):
        x0 = int((bkgx / -8) % 64 + i * 32) * SCALE
        k7.rectfill(x0, 96 * SCALE, x0 + 64, 256, 5)
    if state >= 1 and pipex > -25 and pipex < 140:
        for i in range(pipey + 1):
            for dx in [0, 8, 16]:
                k7.spr(27, pipex * SCALE + dx, i * 8 * SCALE, 1, 1, 0, 0)
        for dx in [0, 8, 16]:
            k7.spr(11, pipex * SCALE + dx, (pipey + 1) * 8 * SCALE, 1, 1, 0, 0)
        for dx in [0, 8, 16]:
            k7.spr(11, pipex * SCALE + dx, (pipey + 1 + gap) * 8 * SCALE, 1, 1, 0, 0)
        for i in range(pipey + 2 + gap, 18):
            for dx in [0, 8, 16]:
                k7.spr(27, pipex * SCALE + dx, i * 8 * SCALE, 1, 1, 0, 0)
    bx, by = px * SCALE, py * SCALE
    if state == 2 or state == 3:
        k7.spr(14, bx, by, 1, 1, 0, 0)
    else:
        bird_spr = 34 if (pv < 0 and (f & 4)) else 32
        k7.spr(bird_spr, bx, by, 1, 1, 0, 0)
    if state == 0:
        k7.print("PRESS X TO FLAP", 48, 96, 7)
        k7.print("FLAPPY!", 72, 64, 7)
    if state > 0:
        score_str = str(score)
        sx = 128 - len(score_str) * 4
        for i, d in enumerate(score_str):
            k7.spr(ord(d) - ord("0") + 1, sx + i * 8, 4, 1, 2, 0, 0)
    if state == 3:
        k7.print("PRESS X TO FLAP", 48, 96, 7)
    k7.draw_to_canvas(CANVAS_ID)

def game_loop():
    update()
    draw()

init()
js.game_loop_js = game_loop

state = "splash"
dots = [[random.randint(0, GW-1), random.randint(0, GH-1)] for _ in range(20)]

def init():
    global config, player, trees, state, dots
    sheet = gfx_to_sheet(GFX_HEX)
    k7.set_sprite_sheet(sheet)
    config = Config()
    player = Player(config)
    trees = [Tree(config) for _ in range(3)]
    state = "splash"
    dots = [[random.randint(0, GW-1), random.randint(0, GH-1)] for _ in range(20)]

def update():
    global state, trees
    if state == "splash":
        if k7.btnp(2) or k7.btnp(4):
            state = "main"
        return
    while player.trail and player.trail[0]["y"] < -10:
        player.trail.pop(0)
    for d in dots:
        d[1] -= config.speed
        if d[1] < 0:
            d[0], d[1] = random.randint(0, GW-1), GH - 1
    player.update()
    config.update()
    to_del = []
    for i, t in enumerate(trees):
        t.update()
        if t.y < -10:
            to_del.append(i)
            trees.append(Tree(config))
        if collides(t, player) and not player.dead:
            player.dead = True
            config.speed = 0
            config.stop = True
            t.y = -150
    for i in sorted(to_del, reverse=True):
        trees.pop(i)
    if len(trees) < 5 and (k7.frame() % 100 == 0):
        trees.append(Tree(config))

def draw():
    k7.cls(7)
    for d in dots:
        k7.rectfill(d[0]*SCALE, d[1]*SCALE, d[0]*SCALE, d[1]*SCALE, 6)
    player.draw()
    for t in trees:
        t.draw()
    k7.rectfill(0, 0, W-1, 24, 0)
    k7.print("SKI  dist: " + str(int(config.dist)), 8, 4, 7)
    if state == "splash":
        k7.print("PRESS O or UP TO START", 32, 100, 11)
    if player.dead:
        k7.print("CRASH! O to restart", 56, 120, 8)
    k7.draw_to_canvas(CANVAS_ID)

def game_loop():
    update()
    draw()

init()
js.game_loop_js = game_loop

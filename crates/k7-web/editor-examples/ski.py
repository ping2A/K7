def gfx_to_sheet(hex_str):
    lines = [L for L in hex_str.strip().split("\n") if len(L) >= 128]
    sheet = bytearray(256 * 64)
    for y in range(min(64, len(lines))):
        for x in range(128):
            sheet[y * 256 + x] = int(lines[y][x], 16)
    return bytes(sheet)


CANVAS_ID = "k7canvas"
W, H = 256, 256
SCALE = 2
GW, GH = W // SCALE, H // SCALE

class Config:
    def __init__(self):
        self.speed = 0.7
        self.dist = 0.0
        self.stop = False
    def update(self):
        if not self.stop:
            self.speed += 0.003
            self.dist += self.speed

class Player:
    def __init__(self, config):
        self.config = config
        self.x = 30.0
        self.y = 40.0
        self.trail = []
        self.dead = False
        self.add_trail()
    def add_trail(self):
        self.trail.append({"x": self.x, "y": self.y})
    def update(self):
        for t in self.trail:
            t["y"] -= self.config.speed
        if not self.dead:
            if k7.btn(0):
                self.x = max(0, self.x - 1.5)
                self.add_trail()
            elif k7.btn(1):
                self.x = min(120, self.x + 1.5)
                self.add_trail()
            else:
                if k7.btnp(2) and self.config.speed >= 0.1:
                    self.config.speed -= 0.1
                if k7.btnp(3):
                    self.config.speed += 0.1
        if self.x < 0: self.x = 0
        if self.x > 120: self.x = 120
    def draw(self):
        for i in range(len(self.trail) - 1):
            a, b = self.trail[i], self.trail[i + 1] if i + 1 < len(self.trail) else {"x": self.x, "y": self.y}
            k7.line(int(a["x"]*SCALE + 2), int(a["y"]*SCALE + 8), int(b["x"]*SCALE + 2), int(b["y"]*SCALE + 8), 6)
            k7.line(int(a["x"]*SCALE + 5), int(a["y"]*SCALE + 8), int(b["x"]*SCALE + 5), int(b["y"]*SCALE + 8), 6)
        n = (18 + (k7.frame() // 8) % 3) if self.dead else 10
        k7.spr(n, int(self.x*SCALE), int(self.y*SCALE), 1, 1, 0, 0)

class Tree:
    def __init__(self, config):
        self.config = config
        self.x = random.randint(0, 18) * 8
        self.y = 127 + random.randint(0, 8) * 8
    def update(self):
        self.y -= self.config.speed
    def draw(self):
        n = 5 + (id(self) % 5)
        k7.spr(n, int(self.x*SCALE), int(self.y*SCALE), 1, 1, 0, 0)

def collides(tree, player):
    bx1, bx2 = player.x + 2, player.x + 6
    by1, by2 = player.y + 5, player.y + 8
    return not ((tree.y + 8 < by1) or (tree.y > by2) or (tree.x + 8 < bx1) or (tree.x > bx2))

config = Config()
